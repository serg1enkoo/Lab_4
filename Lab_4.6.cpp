#include <iostream> 
using namespace std; 

int main(void) { 
    bool answer; // Оголошуємо булеву змінну 'answer' для зберігання результату умови
    int value;   // Оголошуємо цілу змінну 'value' для зберігання введеного значення

    cout << "Enter a value: "; // Виводимо запит до користувача на введення значення
    cin >> value; // Зчитуємо введене користувачем значення і зберігаємо його у змінній 'value'

    // Оцінюємо складну умову і присвоюємо результат (true або false) змінній 'answer'
    answer = (value >= 0 && value < 10) || // Перше умова: значення має бути в діапазоні від 0 до 9 включно
        ((value * 2 < 20) && (value - 2 > -2)) || // Друге умова: (value * 2 < 20) і (value - 2 > -2)
        ((value - 1 > 1) && (value / 2 < 10)) || // Третє умова: (value - 1 > 1) і (value / 2 < 10)
        (value == 111); // Четверте умова: значення має бути точно 111

    // Виводимо результат на основі булевої змінної 'answer'
    cout << (answer ? "THAT'S TRUE :)" : "THAT'S NOT TRUE :(") << endl;
    // Якщо 'answer' є true, виводимо "THAT'S TRUE :)"
    // Якщо 'answer' є false, виводимо "THAT'S NOT TRUE :("

    return 0; // Повертаємо 0, щоб вказати, що програма завершилась успішно
}